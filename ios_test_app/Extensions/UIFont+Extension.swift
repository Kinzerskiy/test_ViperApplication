import UIKit

extension UIFont {
    static func arimo() -> UIFont? {
        return UIFont.init(name: "Arimo-Bold", size: 20)
    }
    
    static func abeezee() -> UIFont? {
        return UIFont.init(name: "ABeeZee-Italic", size: 20)
    }
}
